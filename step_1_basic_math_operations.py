# Create 2 variables, they are both integers
number_1 = 5
number_2 = 10

# ---- ADDITION ----
# Create a NEW variable, the value of which will be number_1 + number_2 which will be 15 (and an integer)
result = number_1 + number_2

# We print the result so we can see it. If we don't print it, we won't know what happened
print(result)


# ---- SUBTRACTION ----
# Create a NEW variable called result, this will overwrite the previous value of result (it will still be an integer)
result = number_1 - number_2
print(result)


# ---- MULTIPLICATION ---
# Create a NEW variable called result again, of course it overwrites the previous value of result, and (still an integer)
result = number_1 * number_2
print(result)


# ---- DIVISION ----
result = number_1 / number_2
# Now we divide, the value is 0.5 which is a FLOAT
print(result)

# ---- EXPONENTS ----
result = number_1 ** number_2
print(result)